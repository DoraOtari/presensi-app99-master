<?php

namespace App\Http\Controllers;
use Illuminate\Support\Facades\DB;
use Illuminate\Http\Request;

class JabatanController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('jabatan.index', ['jabatan' => DB::table('jabatan')->get()]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('jabatan.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
       DB::table('jabatan')->insert([
        'nama' => $request->nama,
        'gaji' => $request->gaji,
        'status' => $request->status,
       ]); // kode memasukan data ke database

       return redirect('/jabatan')->with('notif', 'Berhasil tambah jabatan baru'); //kode kembali ke hal sebelumnya
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $jabatan = DB::table('jabatan')->find($id);
        return view('jabatan.edit', compact('jabatan'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        DB::table('jabatan')->where('id', $id)->update([
            'nama' => $request->nama,
            'gaji' => $request->gaji,
            'status' => $request->status,
           ]);

           return redirect('/jabatan')->with('notif', 'Berhasil update   jabatan baru'); //kode kembali ke hal sebelumnya
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        DB::table('jabatan')
            ->delete($id);

        return redirect()->back()->with('notif', 'Berhasil hapus data jabatan');
    }
}